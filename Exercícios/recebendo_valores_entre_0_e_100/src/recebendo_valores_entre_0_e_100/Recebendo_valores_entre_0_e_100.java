/*
 * 5 - Utilizando a estrutura de repetição while, 
faça um programa que escreva todos os números entre 0 e 100.

 */
package recebendo_valores_entre_0_e_100;


public class Recebendo_valores_entre_0_e_100 {

   
    public static void main(String[] args) {
     int num1 = 1;
     while(num1 <=100){
            System.out.println("=="+num1+"==");
         ++num1; // pré_incremento
      
     }
    }
    
}
